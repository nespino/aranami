  <h2>Emergencias</h2>
  <h4>Síntomas de intoxicación</h4>

  <p>Los síntomas pueden ser: irritación de la nariz, garganta, piel u ojos, 
    o aparecen más generalizados, fatiga y mareos, excesiva salivación, vómitos y diarreas, 
    vértigo y convulsiones, contracciones musculares, problemas respiratorios, cardíacos y neurológicos. 
    De acuerdo al grado de exposición a los agroquímicos se requerirá mayor menor urgencia en el tratamiento.</p>

  <h4>Vías de entrada al organismo</h4>
  <ul>
    <li>Vía digestiva- Considerada secundaria debido a que tan solo se produce en casos accidentales o por falta de medidas higiénicas básicas.</li>
    <li>Vía respiratoria- Mediante vapores, gases, y aerosoles presentes en el ambiente.</li>
    <li>Vía dérmica (piel)- Es la más importante y más infravalorada, debido a que todas las partes del cuerpo que no se mantengan protegidas o que estén mal protegidas son los puntos más peligrosos. La absorción dermal de las diferentes parte del cuerpo varía. Se asigna valor 1 a los antebrazos, que son los de menor absorción, a partir de este se analiza la de las otras partes del cuerpo.</li>
  </ul>

  <h4>Primeros auxilios</h4>
  <ul>
    <li>Dejar el trabajo inmediatamente.</li>
    <li>Cambiarse de ropa inmediatamente.</li>
    <li>Lavarse con agua abundante las zonas de piel u ojos afectados.</li>
    <li> Facilitar la respiración a la persona afectada.</li>
    <li>No dejar nunca sola a la persona afectada.</li>
    <li>No beber ni leche ni alcohol.</li>
    <li>Ir rápidamente al médico con la etiqueta del producto que manipulaba o llamar al CENTRO DE INTOXICACIONES ( 54-11)4962-2247 o HOSPITAL GUTIERREZ (54-11) 4962-6666 o EMERGENCIAS MEDICAS (54-11) 107.</li>
  </ul>
