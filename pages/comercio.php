<h2>Aranami S.A. es una empresa de comercio exterior dividido en dos grandes actividades</h2>

<h4>Agroquímicos</h4>
<p>	
Importación de materia primas, formulación y distribución para los productores agrícolas del País Hoy disponemos registros de los siguientes productos.</p>
	
<p> Herbicidas: Atrazina, Grifosato, Metsulfurón, 2.4 D amina, Dicamba y Clorimurón.
Insecticidas:Cipermetrinas, Fenitrotión, Endosulfán, Metamidofós, Clorpirifós, Dimetoato, Imidacloprid y Lambdacialotrina.
Funguicidas:Tebuconazol, Carbendazin, Carbendazim+Tiram, Diniconazole+TMTD, Metil-tiofanato y Mancozeb.</p>
 	
<p>Es nuestro deseo trabajar con más productos genéricos y  desarrollar nuevos no sólo agroquímicos si no también fertilizantes, productos biológicos, semillas, hormonales y otros para el campo. 
  Hemos trabajado durante tres décadas haciendo desarrollo, registros y comercializando estos rubros de productos tanto en la Argentina, Uruguay, Paraguay y Bolivia para diferentes empresas del mundo 
  con lo cual hemos adquirido y disponemos de la suficiente experiencia y contactos en estos mercados.</p>

<h4>Comercio Exterior</h4>
<p>Hemos trabajado en la empresa especializada en el comercio exterior de origen japonés (Sogoshosha) por ello conocemos muy bién todos los tipos de comercios y productos de exportación e importación desde  y hacia  Argentina , Uruguay , Paraguay y Japón además de otros paises asiáticos.</p>
<p>Ya hemos trabajado como agentes de algunas empresas del Japón, China, USA, Brasil y de nuestro País, para exportar e importar.</p>
<p>Tenemos experiencia en la exportación de carnes, pescados , miel, vinos, granitos, harina de carne y huesos, harina de pescados y otros tan disímiles como equipos de maquinarias y, comunicaciones.</p>
<p>También disponemos de la capacidad y estructura para poder realizar  trabajos de cobranzas, control de embarques y descargas y, estudio de mercados entre otros.</p>

